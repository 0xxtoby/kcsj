package bean;

public class Category {
    int id;
    String title;
    String created_ar;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCreated_ar() {
        return created_ar;
    }

    public void setCreated_ar(String created_ar) {
        this.created_ar = created_ar;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }
}
